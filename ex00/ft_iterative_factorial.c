int	ft_iterative_factorial(int nb)
{
	int	i;

	i = nb - 1;
	if (nb < 0)
		return (0);
	else if (nb == 1 || nb == 0)
		return (1);
	while (i >= 1)
	{
		nb *= i;
		i--;
	}
	return (nb);
}
