int	ft_is_prime(int nb)
{
	int	i;

	i = 3;
	if (nb == 0 || nb == 1 || nb < 0)
		return (0);
	else if (nb == 2)
		return (1);
	else if (nb % 2 == 0)
		return (0);
	while (i < nb / 2)
	{
		if (i % 2 == 1)
		{
			if (nb % i == 0)
				return (0);
		}
		i++;
	}
	return (1);
}
